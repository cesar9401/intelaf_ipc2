    /**
     * Metodo para agregar a los empleados a la tabla, traidos de la base de datos
     */
    private void setTableTiendas() {
        listTiendas = new String[tiendas.size()][6];

        for(int i=0; i<tiendas.size(); i++) {
            listTiendas[i][0] = tiendas.get(i).getCodigo();
            listTiendas[i][1] = tiendas.get(i).getNombre();
            listTiendas[i][2] = tiendas.get(i).getTelefono1();
            listTiendas[i][3] = tiendas.get(i).getTelefono2();
            listTiendas[i][4] = tiendas.get(i).getEmail();
            listTiendas[i][5] = tiendas.get(i).getHorario();
        }
        tiendasTable.setPreferredSize(new java.awt.Dimension(700, 36*tiendas.size()));
        tiendasTable.setModel(new javax.swing.table.DefaultTableModel(
            listTiendas,
            new String [] {
                "Codigo", "Nombre", "Telefono 1", "Telefono 2", "Email", "Horario"
            }
        ));
    }
    
