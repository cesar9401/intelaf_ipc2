    /**
     * Metodo para inicializar componentes necesarios para mostrar informacion
     * de las tiendas de donde se hacen pedidos
     *
     * @param control
     * @param cliente
     */
    public void initializeComponents(MainControl control, Cliente cliente) {
        this.control = control;
        this.cliente = cliente;
        this.tiendasCombo.setVisible(true);
        this.tiendas = this.control.getTiendas();
        if (!tiendas.isEmpty()) {
            tiendas.forEach((t) -> {
                tiendasCombo.addItem(t.getCodigo());
            });
        }
        actualizarDatos();
    }

    /**
     * Evento al hacer click sobre la tabla de productos Muestra la descripcion
     * del producto
     *
     * @param evt
     */
    private void productosTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productosTableMouseClicked
        // TODO add your handling code here:
        int row = productosTable.getSelectedRow();
        if (row != -1) {
            descriptionLabel.setText(productos.get(row).getDescripcion());
        }
    }//GEN-LAST:event_productosTableMouseClicked

    private void tiendasComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tiendasComboItemStateChanged
        // TODO add your handling code here:
        actualizarDatos();
    }//GEN-LAST:event_tiendasComboItemStateChanged

    private void actualizarDatos() {
        //Limpiar listas
        productos.clear();

        //Obtener productos de la base de datos
        if (!tiendas.isEmpty()) {
            String codigo = tiendasCombo.getSelectedItem().toString();
            productos = this.control.getProductosTienda(codigo);
        }

        //Actualizar tablas
        setTableProductos();
    }

    /**
     * Metodo para agregar los productos de la tienda traidos de la base de
     * datos a la tabla
     */
    private void setTableProductos() {
        lista = new String[productos.size()][6];
        for (int i = 0; i < productos.size(); i++) {
            lista[i][0] = productos.get(i).getCodigoProductos();
            lista[i][1] = productos.get(i).getNombre();
            lista[i][2] = productos.get(i).getFabricante();
            lista[i][3] = "Q. " + productos.get(i).getPrecio() + "";
            lista[i][4] = productos.get(i).getGarantia() + "";
            lista[i][5] = productos.get(i).getStock() + "";
        }
        productosTable.setPreferredSize(new java.awt.Dimension(550, 36 * productos.size()));
        productosTable.setModel(new javax.swing.table.DefaultTableModel(
                lista,
                new String[]{
                    "Codigo", "Nombre", "Fabricante", "Precio", "Garantia", "Existencias"
                }
        ));
    }

